						sekaligus symbol status sosial masyarakat Batak Toba. Rumah ini digunakan sebagai tempat untuk
						melakukan rapat atau pertemuan keluarga besar atau Penetua (Natua-tuani Huta) untuk membahas
						sesuatu hal disebut juga sebagai rumah pertemuan (Balai desa). Rumah Bolon ini berbentuk seperti
